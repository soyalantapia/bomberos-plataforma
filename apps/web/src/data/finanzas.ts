// ============================================================
// HELPER: generar fecha ISO restando N días
// ============================================================
function dh(diasAtras: number, hora = '10:00'): string {
  const d = new Date('2026-05-25T12:00:00');
  d.setDate(d.getDate() - diasAtras);
  return `${d.toISOString().slice(0, 10)}T${hora}:00`;
}

