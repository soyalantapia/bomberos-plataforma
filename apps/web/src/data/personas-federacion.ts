import type { EspecialidadBombero, Persona } from '@faro/types';

function mkPersona(
  id: string,
  cuartelId: string,
  legajo: string,
  nombre: string,
  apellido: string,
  jerarquia: Persona['jerarquia'],
  funcion: string,
  perfiles: Persona['perfiles'],
  cuerpo: 'activo' | 'administrativo',
  especialidades?: EspecialidadBombero[],
  tel?: string,
): Persona {
  return {
    id,
    cuartelId,
    legajo,
    nombre,
    apellido,
    email: `${apellido.toLowerCase().replace(/ /g, '')}@bv-${cuartelId.replace('cuartel-', '')}.org`,
    telefono:
      tel ??
      `+54 11 ${Math.floor(4000 + Math.random() * 5999)} ${Math.floor(1000 + Math.random() * 8999)}`,
    fechaNacimiento: '1985-01-01',
    fechaIngreso: '2010-01-01',
    jerarquia,
    estado: 'activo',
    base: 'Cuartel Central',
    funcion,
    perfiles,
    cuerpo,
    especialidades,
    salud: { grupoSanguineo: 'O+', aptitudVencimiento: '2026-12-01' },
    cursos: [],
  };
}

